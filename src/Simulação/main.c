/* Calcula o índice linear (endereço) de matrix[row][col] */
static inline uint32_t mat_addr(int row, int col) {
    return MATRIX_BASE + (uint32_t)(row * MATRIX_COLS + col);
}

/* Carrega a matriz SEED na RAM simulada */
static void load_matrix(MemorySystem *sys) {
    for (int i = 0; i < MATRIX_ROWS; i++)
        for (int j = 0; j < MATRIX_COLS; j++)
            sys->ram[mat_addr(i, j)] = SEED[i][j];
}

/* Imprime os resultados de L1 e L2 com taxa de acerto */
static void print_stats(MemorySystem *sys) {
    int l1h = sys->stats_L1.hits,   l1m = sys->stats_L1.misses;
    int l2h = sys->stats_L2.hits,   l2m = sys->stats_L2.misses;
    int l1t = l1h + l1m;
    int l2t = l2h + l2m;
    double l1r = l1t ? 100.0 * l1h / l1t : 0.0;
    double l2r = l2t ? 100.0 * l2h / l2t : 0.0;

    printf("L1 - Hits: %d \t Misses: %d \t Hit Rate: %.2f\n", l1h, l1m, l1r);
    printf("L2 - Hits: %d \t Misses: %d \t Hit Rate: %.2f\n", l2h, l2m, l2r);
}

