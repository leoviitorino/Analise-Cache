/*
 * Fazemos duas buscas idênticas (soma de todos os elementos) mas com ordens de acesso diferentes:
 * Acesso por linha, e depois 2) Acesso por coluna.
 */
void columnsXrows(MemorySystem *sys) {
    int val = 0;
    long long sum = 0;

    printf("\n\tLinha vs Coluna\n");

    /* Acesso por linha */
    printf("\nAcesso por linha\n");
    init_memory(sys);
    sum = 0;
    for (int i = 0; i < MATRIX_ROWS; i++)
        for (int j = 0; j < MATRIX_COLS; j++) {
            read_memory(sys, mat_addr(i, j), &val);
            sum += val;
        }
    print_stats(sys);

    /* Acesso por coluna */
    printf("\nAcesso por coluna\n");
    init_memory(sys); 
    sum = 0;
    for (int j = 0; j < MATRIX_COLS; j++)
        for (int i = 0; i < MATRIX_ROWS; i++) {
            read_memory(sys, mat_addr(i, j), &val);
            sum += val;
        }
    print_stats(sys);
}

